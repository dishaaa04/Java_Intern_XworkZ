package com.xworkz.casting;

public class HardDiskDrive {

    public void capacity() {
        System.out.println("Capacity: 1TB");
    }

    public void type() {
        System.out.println("Type: Internal HDD");
    }

    public void rpm() {
        System.out.println("RPM: 7200");
    }

    public void readWriteSpeed() {
        System.out.println("Read/Write Speed: 150MB/s");
    }
}
