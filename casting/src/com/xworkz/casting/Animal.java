package com.xworkz.casting;

public class Animal {

    public void eat() {
        System.out.println("Animal is eating.");
    }

    public void sleep() {
        System.out.println("Animal is sleeping.");
    }

    public void type() {
        System.out.println("This is a generic animal.");
    }

    public void habitat() {
        System.out.println("Lives in natural habitat.");
    }
}
