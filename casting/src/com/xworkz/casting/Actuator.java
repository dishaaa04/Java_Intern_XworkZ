package com.xworkz.casting;

public class Actuator {

    public void activate() {
        System.out.println("Activating actuator...");
    }

    public void deactivate() {
        System.out.println("Deactivating actuator...");
    }

    public void type() {
        System.out.println("Generic actuator type.");
    }

    public void powerSource() {
        System.out.println("Default power source.");
    }
}
