package com.xworkz.casting;

public class Cat {

    public void makeSound() {
        System.out.println("Cat says: Meow!");
    }

    public void sleep() {
        System.out.println("Cat is sleeping.");
    }

    public void type() {
        System.out.println("Generic domestic cat.");
    }

    public void age() {
        System.out.println("Age: 2 years.");
    }
}

