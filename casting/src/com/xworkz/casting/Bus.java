package com.xworkz.casting;

public class Bus {

    public void start() {
        System.out.println("Bus is starting its journey.");
    }

    public void stop() {
        System.out.println("Bus is stopping at the station.");
    }

    public void type() {
        System.out.println("Generic transport bus.");
    }

    public void capacity() {
        System.out.println("Capacity: 40 passengers.");
    }
}
