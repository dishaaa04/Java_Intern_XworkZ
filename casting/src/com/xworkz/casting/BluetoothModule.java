package com.xworkz.casting;

public class BluetoothModule {

    public void powerOn() {
        System.out.println("Bluetooth module is powered on.");
    }

    public void powerOff() {
        System.out.println("Bluetooth module is powered off.");
    }

    public void version() {
        System.out.println("Bluetooth version: Generic.");
    }

    public void dataRate() {
        System.out.println("Default data rate: 1 Mbps.");
    }
}

