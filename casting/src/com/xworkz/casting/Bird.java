package com.xworkz.casting;

public class Bird {

    public void fly() {
        System.out.println("Bird is flying.");
    }

    public void eat() {
        System.out.println("Bird is eating grains.");
    }

    public void type() {
        System.out.println("Generic bird.");
    }

    public void habitat() {
        System.out.println("Bird lives in trees.");
    }
}
