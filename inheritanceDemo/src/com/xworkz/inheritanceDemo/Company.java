package com.xworkz.inheritanceDemo;

public class Company {

    Company() {
        System.out.println("Welcome to the company");
    }

    public void work() {
        System.out.println("Employees work daily");
    }

    public void meeting() {
        System.out.println("Meetings are scheduled regularly");
    }

    public void projects() {
        System.out.println("Handling various projects");
    }

    public void salary() {
        System.out.println("Employees receive salaries on time");
    }

    public void benefits() {
        System.out.println("Company provides various benefits");
    }
}
