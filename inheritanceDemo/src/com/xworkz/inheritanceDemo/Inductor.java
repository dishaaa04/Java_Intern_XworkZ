package com.xworkz.inheritanceDemo;

public class Inductor {

    public Inductor() {
        System.out.println("Inductor is initialized");
    }

    public void storeEnergy() {
        System.out.println("Inductor stores energy in magnetic field");
    }

    public void opposeCurrentChange() {
        System.out.println("Inductor opposes change in current");
    }

    public void generateMagneticField() {
        System.out.println("Inductor generates magnetic field");
    }

    public void coilTurns() {
        System.out.println("Inductor has multiple turns of coil");
    }

    public void reactToAC() {
        System.out.println("Inductor reacts to alternating current");
    }
}
