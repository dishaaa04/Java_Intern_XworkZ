package com.xworkz.inheritanceDemo;

public class Family {
    Family() {
        System.out.println("Family class");
    }

    public void bonding() {
        System.out.println("Family members bond with each other");
    }

    public void support() {
        System.out.println("Family supports in tough times");
    }

    public void surname() {
        System.out.println("Family shares a common surname");
    }

    public void traditions() {
        System.out.println("Family follows traditions");
    }

    public void values() {
        System.out.println("Family teaches values");
    }
}
