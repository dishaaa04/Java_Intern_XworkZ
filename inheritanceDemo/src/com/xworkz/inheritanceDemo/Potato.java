package com.xworkz.inheritanceDemo;

public class Potato {

    public Potato() {
        System.out.println("Potato is born underground");
    }

    public void grow() {
        System.out.println("Potato grows in soil");
    }

    public void beBoiled() {
        System.out.println("Potato is being boiled");
    }

    public void beFried() {
        System.out.println("Potato is being fried");
    }

    public void beMashed() {
        System.out.println("Potato is being mashed");
    }

    public void beEaten() {
        System.out.println("Potato is being eaten");
    }
}
