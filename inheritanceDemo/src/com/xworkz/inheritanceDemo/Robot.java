package com.xworkz.inheritanceDemo;

public class Robot {

    public Robot() {
        System.out.println("Robot is activated");
    }

    public void move() {
        System.out.println("Robot is moving");
    }

    public void scanArea() {
        System.out.println("Robot is scanning the area");
    }

    public void pickObjects() {
        System.out.println("Robot is picking up objects");
    }

    public void recharge() {
        System.out.println("Robot is recharging");
    }

    public void stop() {
        System.out.println("Robot has stopped");
    }
}
