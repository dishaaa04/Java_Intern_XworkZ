package com.xworkz.inheritanceDemo;

public class Drone {

    public void startEngine() {
        System.out.println("Drone engine starts");
    }

    public void fly() {
        System.out.println("Drone flies in the air");
    }

    public void capturePhotos() {
        System.out.println("Drone captures aerial photos");
    }

    public void streamVideo() {
        System.out.println("Drone streams live video");
    }

    public void land() {
        System.out.println("Drone lands safely");
    }
}
