package com.xworkz.inheritanceDemo;

public class MotherBoard {

    public MotherBoard() {
        System.out.println("MotherBoard is initialized");
    }

    public void connectComponents() {
        System.out.println("MotherBoard connects all hardware components");
    }

    public void providePower() {
        System.out.println("MotherBoard distributes power to components");
    }

    public void supportCPU() {
        System.out.println("MotherBoard supports CPU installation");
    }

    public void manageBIOS() {
        System.out.println("MotherBoard manages BIOS settings");
    }

    public void controlDataFlow() {
        System.out.println("MotherBoard controls data flow between devices");
    }
}
