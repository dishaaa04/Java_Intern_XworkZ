package com.xworkz.inheritanceDemo;

public class Bread {

    public void slice() {
        System.out.println("Bread is sliced");
    }

    public void toast() {
        System.out.println("Bread is toasted");
    }

    public void pack() {
        System.out.println("Bread is packed in plastic wrap");
    }

    public void checkFreshness() {
        System.out.println("Checking freshness of the bread");
    }

    public void serve() {
        System.out.println("Bread is ready to be served");
    }
}
