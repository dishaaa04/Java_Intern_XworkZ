package com.xworkz.inheritanceDemo;

public class Refrigerator {

    public void powerOn() {
        System.out.println("Refrigerator is powered on");
    }

    public void coolItems() {
        System.out.println("Refrigerator cools stored food and drinks");
    }

    public void defrost() {
        System.out.println("Refrigerator defrosts ice in the freezer");
    }

    public void adjustTemperature() {
        System.out.println("Refrigerator temperature is adjusted");
    }

    public void storeFood() {
        System.out.println("Refrigerator stores food to preserve freshness");
    }
}
