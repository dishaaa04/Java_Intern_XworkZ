package com.xworkz.inheritanceDemo;

public class Toaster {

    public void powerOn() {
        System.out.println("Toaster is powered on");
    }

    public void insertBread() {
        System.out.println("Bread is inserted into the toaster");
    }

    public void heatElements() {
        System.out.println("Toaster heats its elements to toast bread");
    }

    public void popUp() {
        System.out.println("Toaster pops up the toasted bread");
    }

    public void powerOff() {
        System.out.println("Toaster is powered off after use");
    }
}

