package com.xworkz.inheritanceDemo;

public class PressureCooker {

    public void addIngredients() {
        System.out.println("Ingredients are added into the pressure cooker");
    }

    public void closeLid() {
        System.out.println("Pressure cooker lid is closed tightly");
    }

    public void buildPressure() {
        System.out.println("Pressure cooker builds pressure with steam");
    }

    public void cookFood() {
        System.out.println("Pressure cooker cooks food faster using steam pressure");
    }

    public void releasePressure() {
        System.out.println("Pressure cooker releases steam after cooking");
    }
}
