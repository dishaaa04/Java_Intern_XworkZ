package com.xworkz.inheritanceDemo;

public class Projector {

    public Projector() {
        System.out.println("Projector is ready");
    }

    public void adjustFocus() {
        System.out.println("Adjusting projector focus");
    }

    public void changeBrightness() {
        System.out.println("Changing projector brightness");
    }

    public void powerOn() {
        System.out.println("Projector is powering on");
    }

    public void powerOff() {
        System.out.println("Projector is powering off");
    }
}
