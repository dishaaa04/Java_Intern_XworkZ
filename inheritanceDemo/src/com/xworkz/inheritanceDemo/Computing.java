package com.xworkz.inheritanceDemo;

public class Computing {

    public Computing() {
        System.out.println("Computing device is initialized");
    }

    public void processData() {
        System.out.println("Computing device processes data");
    }

    public void storeData() {
        System.out.println("Computing device stores data");
    }

    public void performCalculation() {
        System.out.println("Computing device performs calculations");
    }

    public void displayOutput() {
        System.out.println("Computing device displays output");
    }

    public void receiveInput() {
        System.out.println("Computing device receives input");
    }
}
