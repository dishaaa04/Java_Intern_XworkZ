package com.xworkz.inheritanceDemo;

public class Microcontroller {

    public Microcontroller() {
        System.out.println("Microcontroller is initialized");
    }

    public void controlDevices() {
        System.out.println("Microcontroller controls connected devices");
    }

    public void executeProgram() {
        System.out.println("Microcontroller executes embedded programs");
    }

    public void communicate() {
        System.out.println("Microcontroller communicates via serial interfaces");
    }

    public void managePower() {
        System.out.println("Microcontroller manages power efficiently");
    }

    public void interactWithSensors() {
        System.out.println("Microcontroller interacts with sensors");
    }
}
