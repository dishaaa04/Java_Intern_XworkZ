package com.xworkz.inheritanceDemo;

public class Network {

    public Network() {
        System.out.println("Network is initialized");
    }

    public void connectDevices() {
        System.out.println("Network connects multiple devices");
    }

    public void transmitData() {
        System.out.println("Network transmits data between systems");
    }

    public void assignIP() {
        System.out.println("Network assigns IP addresses");
    }

    public void maintainStability() {
        System.out.println("Network maintains stability and speed");
    }

    public void enableCommunication() {
        System.out.println("Network enables digital communication");
    }
}
