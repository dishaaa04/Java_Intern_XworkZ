package com.xworkz.inheritanceDemo;

public class RaspberryPiCamera {

    public void initializeCamera() {
        System.out.println("Raspberry Pi Camera is initialized");
    }

    public void captureImage() {
        System.out.println("Raspberry Pi Camera captures an image");
    }

    public void recordVideo() {
        System.out.println("Raspberry Pi Camera records video");
    }

    public void adjustFocus() {
        System.out.println("Raspberry Pi Camera adjusts focus automatically");
    }

    public void streamFeed() {
        System.out.println("Raspberry Pi Camera streams live feed");
    }
}
