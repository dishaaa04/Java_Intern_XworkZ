package com.xworkz.inheritanceDemo;

public class Medicine {

    public Medicine() {
        System.out.println("Medicine is prescribed");
    }

    public void cureIllness() {
        System.out.println("Medicine helps to cure illness");
    }

    public void dosage() {
        System.out.println("Take medicine as per dosage");
    }

    public void sideEffects() {
        System.out.println("Medicine may have side effects");
    }

    public void expiryDate() {
        System.out.println("Check the expiry date before use");
    }

    public void storageConditions() {
        System.out.println("Store the medicine properly");
    }
}
