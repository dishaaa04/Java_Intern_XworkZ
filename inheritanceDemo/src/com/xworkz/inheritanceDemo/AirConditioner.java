package com.xworkz.inheritanceDemo;

public class AirConditioner {

    public void powerOn() {
        System.out.println("Air conditioner is powered on");
    }

    public void setTemperature() {
        System.out.println("Setting the desired room temperature");
    }

    public void startCooling() {
        System.out.println("Air conditioner starts cooling the room");
    }

    public void swingFlaps() {
        System.out.println("Flaps are swinging to distribute cool air");
    }

    public void powerOff() {
        System.out.println("Air conditioner is powered off");
    }
}
