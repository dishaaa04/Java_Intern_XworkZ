package com.xworkz.inheritanceDemo;

public class WallCalendar {

    public void showDate() {
        System.out.println("Wall calendar shows the current date");
    }

    public void markHolidays() {
        System.out.println("Wall calendar has holidays marked for easy reference");
    }

    public void flipPage() {
        System.out.println("Flipping to the next month in the wall calendar");
    }

    public void hangOnWall() {
        System.out.println("Wall calendar is hanging on the wall");
    }

    public void checkDay() {
        System.out.println("Checking the day of the week on the calendar");
    }
}
