package com.xworkz.inheritanceDemo;

public class Bookshelf {

    public void storeBooks() {
        System.out.println("Bookshelf stores a variety of books");
    }

    public void organizeShelves() {
        System.out.println("Bookshelf has organized shelves for categories");
    }

    public void supportWeight() {
        System.out.println("Bookshelf supports the weight of many books");
    }

    public void cleanDust() {
        System.out.println("Cleaning dust from bookshelf surfaces");
    }

    public void displayItems() {
        System.out.println("Bookshelf also displays decorative items");
    }
}
