package com.xworkz.inheritanceDemo;

public class Magic {

    public Magic() {
        System.out.println("Magic is being used");
    }

    public void castSpell() {
        System.out.println("Casting a basic spell");
    }

    public void createIllusion() {
        System.out.println("Creating an illusion");
    }

    public void summonCreature() {
        System.out.println("Summoning a creature");
    }

    public void controlElements() {
        System.out.println("Controlling the elements");
    }

    public void disappear() {
        System.out.println("Performing a disappearing act");
    }
}
