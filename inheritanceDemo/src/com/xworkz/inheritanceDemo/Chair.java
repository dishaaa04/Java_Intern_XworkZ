package com.xworkz.inheritanceDemo;

public class Chair {

    public void provideSeating() {
        System.out.println("Chair provides seating support");
    }

    public void adjustHeight() {
        System.out.println("Chair height can be adjusted");
    }

    public void supportBack() {
        System.out.println("Chair supports the back for comfort");
    }

    public void movePosition() {
        System.out.println("Chair can be moved from one place to another");
    }

    public void rotate() {
        System.out.println("Chair rotates for flexible movement");
    }
}

