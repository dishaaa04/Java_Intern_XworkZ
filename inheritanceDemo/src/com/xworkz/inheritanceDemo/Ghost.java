package com.xworkz.inheritanceDemo;

public class Ghost {

    public Ghost() {
        System.out.println("A ghost appears...");
    }

    public void haunt() {
        System.out.println("Ghost is haunting the house...");
    }

    public void disappear() {
        System.out.println("Ghost vanishes into thin air...");
    }

    public void makeWeirdNoises() {
        System.out.println("Ghost makes eerie sounds...");
    }

    public void possessObjects() {
        System.out.println("Ghost is possessing random objects...");
    }

    public void floatAround() {
        System.out.println("Ghost floats around silently...");
    }
}
