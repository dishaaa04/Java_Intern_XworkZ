package com.xworkz.inheritanceDemo;

public class Gearbox {

    public void changeGear() {
        System.out.println("Gearbox changes the gear ratio");
    }

    public void transferPower() {
        System.out.println("Gearbox transfers power to the wheels");
    }

    public void reduceSpeed() {
        System.out.println("Gearbox reduces the speed");
    }

    public void increaseTorque() {
        System.out.println("Gearbox increases the torque");
    }

    public void engageClutch() {
        System.out.println("Gearbox engages the clutch");
    }
}
