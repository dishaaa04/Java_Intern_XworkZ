package com.xworkz.inheritanceDemo;

public class Resort {

    public Resort() {
        System.out.println("Resort is ready for guests");
    }

    public void bookRoom() {
        System.out.println("Room booked at the resort");
    }

    public void checkIn() {
        System.out.println("Guest checked in at the resort");
    }

    public void checkOut() {
        System.out.println("Guest checked out from the resort");
    }

    public void enjoyFacilities() {
        System.out.println("Enjoying resort facilities");
    }
}
