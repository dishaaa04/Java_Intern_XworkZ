package com.xworkz.inheritanceDemo;

public class Teacher {

    public Teacher() {
        System.out.println("Teacher is present in the classroom");
    }

    public void teach() {
        System.out.println("Teacher is teaching students");
    }

    public void giveAssignment() {
        System.out.println("Teacher is giving assignments");
    }

    public void takeAttendance() {
        System.out.println("Teacher is taking attendance");
    }

    public void evaluatePapers() {
        System.out.println("Teacher is evaluating papers");
    }

    public void provideFeedback() {
        System.out.println("Teacher is providing feedback");
    }
}
