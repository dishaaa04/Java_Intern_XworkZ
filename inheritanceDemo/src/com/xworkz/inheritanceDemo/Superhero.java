package com.xworkz.inheritanceDemo;

public class Superhero {

    public Superhero() {
        System.out.println("Superhero is ready to save the world");
    }

    public void fly() {
        System.out.println("Superhero is flying");
    }

    public void fightVillains() {
        System.out.println("Superhero is fighting villains");
    }

    public void useSuperStrength() {
        System.out.println("Superhero is using super strength");
    }

    public void savePeople() {
        System.out.println("Superhero is saving people");
    }

    public void disguiseIdentity() {
        System.out.println("Superhero is disguising identity");
    }
}
