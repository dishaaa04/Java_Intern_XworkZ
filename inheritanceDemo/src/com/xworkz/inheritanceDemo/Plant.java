package com.xworkz.inheritanceDemo;

public class Plant {

    public Plant() {
        System.out.println("Plant object created");
    }

    public void grow() {
        System.out.println("Plant is growing");
    }

    public void absorbWater() {
        System.out.println("Plant is absorbing water");
    }

    public void photosynthesis() {
        System.out.println("Plant is performing photosynthesis");
    }

    public void oxygenRelease() {
        System.out.println("Plant is releasing oxygen");
    }

    public void provideShade() {
        System.out.println("Plant is providing shade");
    }
}
