package com.xworkz.inheritanceDemo;

public class Job {

    public Job() {
        System.out.println("Job is created");
    }

    public void work() {
        System.out.println("Doing general job work");
    }

    public void attendMeetings() {
        System.out.println("Attending regular meetings");
    }

    public void receiveSalary() {
        System.out.println("Receiving monthly salary");
    }

    public void getPromotion() {
        System.out.println("Getting promoted based on performance");
    }

    public void takeLeave() {
        System.out.println("Taking paid or unpaid leave");
    }
}
