package com.xworkz.inheritanceDemo;

public class Saucepan {

    public void addIngredients() {
        System.out.println("Ingredients are added into the saucepan");
    }

    public void heatContents() {
        System.out.println("Saucepan heats the contents on a stove");
    }

    public void stirContents() {
        System.out.println("Saucepan contents are stirred while cooking");
    }

    public void pourOut() {
        System.out.println("Saucepan pours out cooked liquid or sauce");
    }

    public void washPan() {
        System.out.println("Saucepan is washed after use");
    }
}
