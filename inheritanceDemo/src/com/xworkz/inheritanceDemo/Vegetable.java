package com.xworkz.inheritanceDemo;

public class Vegetable {

    public Vegetable() {
        System.out.println("Vegetable is ready to be cooked");
    }

    public void cook() {
        System.out.println("Vegetable is being cooked");
    }

    public void grow() {
        System.out.println("Vegetable is growing in the field");
    }

    public void harvest() {
        System.out.println("Vegetable is being harvested");
    }

    public void nutrients() {
        System.out.println("Vegetable contains nutrients");
    }

    public void sell() {
        System.out.println("Vegetable is being sold");
    }
}
