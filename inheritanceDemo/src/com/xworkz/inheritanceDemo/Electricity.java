package com.xworkz.inheritanceDemo;

public class Electricity {
    Electricity() {
        System.out.println("Electricity class");
    }

    public void generate() {
        System.out.println("Electricity is being generated");
    }

    public void transmit() {
        System.out.println("Electricity is being transmitted");
    }

    public void store() {
        System.out.println("Electricity is stored in batteries");
    }

    public void shock() {
        System.out.println("Electricity can cause shocks");
    }

    public void powerDevices() {
        System.out.println("Electricity powers devices");
    }
}
