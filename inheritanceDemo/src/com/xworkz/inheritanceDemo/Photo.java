package com.xworkz.inheritanceDemo;

public class Photo {

    public Photo() {
        System.out.println("Photo object created");
    }

    public void gallery() {
        System.out.println("Photo is stored in the gallery");
    }

    public void click() {
        System.out.println("Clicking a photo");
    }

    public void memories() {
        System.out.println("Photo captures memories");
    }

    public void phone() {
        System.out.println("Photo taken on phone");
    }

    public void safe() {
        System.out.println("Photo is stored safely");
    }
}
