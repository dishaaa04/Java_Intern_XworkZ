package com.xworkz.inheritanceDemo;

public class Festival {
    Festival() {
        System.out.println("Festival class");
    }

    public void clothes() {
        System.out.println("Wearing traditional clothes during festival");
    }

    public void food() {
        System.out.println("Enjoying delicious food during festival");
    }

    public void happy() {
        System.out.println("Everyone feels happy during festival");
    }

    public void share() {
        System.out.println("Sharing gifts and sweets during festival");
    }

    public void sweets() {
        System.out.println("Sweets are an important part of the festival");
    }
}
