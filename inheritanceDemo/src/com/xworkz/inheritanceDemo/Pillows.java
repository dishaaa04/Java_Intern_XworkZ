package com.xworkz.inheritanceDemo;

public class Pillows {

    public void provideSupport() {
        System.out.println("Pillows provide head and neck support");
    }

    public void adjustShape() {
        System.out.println("Pillows can be adjusted for better comfort");
    }

    public void changeCovers() {
        System.out.println("Changing pillow covers for cleanliness");
    }

    public void fluff() {
        System.out.println("Fluffing the pillows to restore shape");
    }

    public void placeOnBed() {
        System.out.println("Pillows are placed on the bed for sleeping");
    }
}
