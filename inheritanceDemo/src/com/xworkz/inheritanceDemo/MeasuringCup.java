package com.xworkz.inheritanceDemo;

public class MeasuringCup {

    public void measureFlour() {
        System.out.println("Measuring cup is used to measure flour");
    }

    public void measureLiquid() {
        System.out.println("Measuring cup is used to measure liquids");
    }

    public void readScale() {
        System.out.println("Reading scale printed on the measuring cup");
    }

    public void pourIngredient() {
        System.out.println("Ingredient is poured from the measuring cup");
    }

    public void cleanCup() {
        System.out.println("Measuring cup is cleaned after use");
    }
}
