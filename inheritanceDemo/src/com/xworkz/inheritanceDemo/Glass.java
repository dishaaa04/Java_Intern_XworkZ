package com.xworkz.inheritanceDemo;

public class Glass {

    public Glass() {
        System.out.println("Glass object is created");
    }

    public void fragile() {
        System.out.println("Glass is fragile");
    }

    public void heatResistance() {
        System.out.println("Glass has some heat resistance");
    }

    public void reflect() {
        System.out.println("Glass reflects light");
    }

    public void material() {
        System.out.println("Glass is made from silica");
    }

    public void transparency() {
        System.out.println("Glass is transparent");
    }
}
