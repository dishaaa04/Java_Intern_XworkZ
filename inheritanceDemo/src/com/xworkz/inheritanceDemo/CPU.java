package com.xworkz.inheritanceDemo;

public class CPU {

    public CPU() {
        System.out.println("CPU is initialized");
    }

    public void processInstructions() {
        System.out.println("CPU processes instructions");
    }

    public void executeProgram() {
        System.out.println("CPU executes the program");
    }

    public void controlOperations() {
        System.out.println("CPU controls system operations");
    }

    public void fetchData() {
        System.out.println("CPU fetches data from memory");
    }

    public void decodeInstructions() {
        System.out.println("CPU decodes instructions");
    }
}
