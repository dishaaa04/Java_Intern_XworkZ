package com.xworkz.inheritanceDemo;


public class Fruit {

    Fruit() {
        System.out.println("A fruit is created");
    }
    public void taste() {

        System.out.println("Fruits have different tastes");
    }
    public void color() {

        System.out.println("Fruits come in various colors");
    }
    public void vitamins() {

        System.out.println("Fruits provide essential vitamins");
    }
    public void grow() {

        System.out.println("Fruits grow on trees or plants");
    }
    public void seasonal() {

        System.out.println("Some fruits are seasonal");
    }
}


