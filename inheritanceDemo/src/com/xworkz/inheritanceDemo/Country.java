package com.xworkz.inheritanceDemo;

public class Country {
    Country() {
        System.out.println("This is a country");
    }

    public void name() {
        System.out.println("Country name: India");
    }

    public void population() {
        System.out.println("Population: 1.4 billion");
    }

    public void nationalAnthem() {
        System.out.println("Jana Gana Mana is the national anthem");
    }

    public void independenceYear() {
        System.out.println("Independence Year: 1947");
    }

    public void currency() {
        System.out.println("Currency: Indian Rupee");
    }
}
