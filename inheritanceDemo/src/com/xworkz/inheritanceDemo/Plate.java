package com.xworkz.inheritanceDemo;

public class Plate {

    public Plate() {
        System.out.println("Plate is created");
    }

    public void shape() {
        System.out.println("Plate has a round shape");
    }

    public void clean() {
        System.out.println("Plate is being cleaned");
    }

    public void hold() {
        System.out.println("Plate holds food");
    }

    public void size() {
        System.out.println("Plate comes in various sizes");
    }

    public void material() {
        System.out.println("Plate can be made of metal, ceramic or plastic");
    }
}
