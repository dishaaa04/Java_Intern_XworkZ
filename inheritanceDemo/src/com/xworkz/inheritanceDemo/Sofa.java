package com.xworkz.inheritanceDemo;

public class Sofa {

    public void provideSeating() {
        System.out.println("Sofa provides comfortable seating");
    }

    public void addCushions() {
        System.out.println("Sofa is decorated with soft cushions");
    }

    public void recline() {
        System.out.println("Sofa can recline for relaxation");
    }

    public void foldOutBed() {
        System.out.println("Sofa can be converted into a bed");
    }

    public void cleanFabric() {
        System.out.println("Cleaning the fabric of the sofa regularly");
    }
}
