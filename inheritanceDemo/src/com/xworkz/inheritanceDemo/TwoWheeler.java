package com.xworkz.inheritanceDemo;

public class TwoWheeler {

    public TwoWheeler() {
        System.out.println("TwoWheeler is initialized");
    }

    public void start() {
        System.out.println("TwoWheeler is starting");
    }

    public void stop() {
        System.out.println("TwoWheeler is stopping");
    }

    public void fuel() {
        System.out.println("TwoWheeler needs fuel");
    }

    public void horn() {
        System.out.println("TwoWheeler horn is active");
    }

    public void accelerate() {
        System.out.println("TwoWheeler is accelerating");
    }
}
