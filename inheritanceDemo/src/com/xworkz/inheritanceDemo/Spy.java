package com.xworkz.inheritanceDemo;

public class Spy {

    public Spy() {
        System.out.println("Spy is on a mission");
    }

    public void gatherIntel() {
        System.out.println("Spy is gathering intelligence");
    }

    public void disguise() {
        System.out.println("Spy is using disguise");
    }

    public void useGadgets() {
        System.out.println("Spy is using gadgets");
    }

    public void escape() {
        System.out.println("Spy is making an escape");
    }

    public void decodeMessage() {
        System.out.println("Spy is decoding the message");
    }
}
