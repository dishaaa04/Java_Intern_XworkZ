package com.xworkz.inheritanceDemo;

public class IoTSensor {

    public void sense() {
        System.out.println("IoTSensor is sensing data");
    }

    public void transmit() {
        System.out.println("IoTSensor is transmitting data");
    }

    public void calibrate() {
        System.out.println("IoTSensor is calibrating");
    }

    public void powerOn() {
        System.out.println("IoTSensor is powered on");
    }

    public void powerOff() {
        System.out.println("IoTSensor is powered off");
    }
}
