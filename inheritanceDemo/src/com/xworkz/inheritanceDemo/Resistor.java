package com.xworkz.inheritanceDemo;

public class Resistor {

    public Resistor() {
        System.out.println("Resistor is ready");
    }

    public void resistCurrent() {
        System.out.println("Resistor resists the flow of current");
    }

    public void generateHeat() {
        System.out.println("Resistor generates heat");
    }

    public void reduceVoltage() {
        System.out.println("Resistor reduces voltage in the circuit");
    }

    public void limitCurrent() {
        System.out.println("Resistor limits the current flow");
    }

    public void ohmsLaw() {
        System.out.println("Resistor follows Ohm's Law");
    }
}
