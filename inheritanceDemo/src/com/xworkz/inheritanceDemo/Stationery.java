package com.xworkz.inheritanceDemo;

public class Stationery {

    public Stationery() {
        System.out.println("Stationery items are essential for work and study");
    }

    public void write() {
        System.out.println("Stationery is used for writing");
    }

    public void draw() {
        System.out.println("Stationery helps in drawing");
    }

    public void store() {
        System.out.println("Stationery is stored in boxes or pouches");
    }

    public void organize() {
        System.out.println("Stationery should be well organized");
    }

    public void use() {
        System.out.println("Stationery is used daily");
    }
}
