package com.xworkz.inheritanceDemo;

public class ConveyorBelt {

    public void moveItems() {
        System.out.println("ConveyorBelt moves items along a path");
    }

    public void startBelt() {
        System.out.println("ConveyorBelt starts moving");
    }

    public void stopBelt() {
        System.out.println("ConveyorBelt stops moving");
    }

    public void adjustSpeed() {
        System.out.println("ConveyorBelt speed is adjusted");
    }

    public void transportGoods() {
        System.out.println("ConveyorBelt transports goods");
    }
}
