package com.xworkz.inheritanceDemo;

public class Alien {

    Alien() {
        System.out.println("Alien species detected");
    }

    public void communicate() {
        System.out.println("Alien communicates using telepathy");
    }

    public void abductHumans() {
        System.out.println("Alien abducts a human for experiments");
    }

    public void controlSpaceship() {
        System.out.println("Alien pilots a spaceship");
    }

    public void regenerate() {
        System.out.println("Alien regenerates lost limbs");
    }

    public void eatRocks() {
        System.out.println("Alien eats space rocks for energy");
    }
}
