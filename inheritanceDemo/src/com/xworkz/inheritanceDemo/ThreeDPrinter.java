package com.xworkz.inheritanceDemo;

public class ThreeDPrinter {

    public void initializePrinter() {
        System.out.println("3D Printer is initialized");
    }

    public void loadMaterial() {
        System.out.println("3D Printer loads material for printing");
    }

    public void startPrinting() {
        System.out.println("3D Printer starts the printing process");
    }

    public void stopPrinting() {
        System.out.println("3D Printer stops the printing process");
    }

    public void displayProgress() {
        System.out.println("3D Printer displays current printing progress");
    }
}
