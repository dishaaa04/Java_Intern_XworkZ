package com.xworkz.inheritanceDemo;

public class Mirror {

    public void reflectImage() {
        System.out.println("Mirror reflects the image of the person or object in front");
    }

    public void mountOnWall() {
        System.out.println("Mirror is mounted on the wall for easy use");
    }

    public void cleanSurface() {
        System.out.println("Cleaning the mirror surface for clear reflection");
    }

    public void adjustAngle() {
        System.out.println("Adjusting the angle of the mirror");
    }

    public void decorateRoom() {
        System.out.println("Mirror enhances room aesthetics");
    }
}
