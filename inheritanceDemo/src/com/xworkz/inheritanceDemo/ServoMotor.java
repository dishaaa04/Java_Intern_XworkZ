package com.xworkz.inheritanceDemo;

public class ServoMotor {

    public void rotate() {
        System.out.println("ServoMotor is rotating to a specific angle");
    }

    public void controlSpeed() {
        System.out.println("ServoMotor is controlling speed");
    }

    public void positionControl() {
        System.out.println("ServoMotor provides position control");
    }

    public void connectPower() {
        System.out.println("ServoMotor is connected to power");
    }

    public void stop() {
        System.out.println("ServoMotor has stopped");
    }
}
