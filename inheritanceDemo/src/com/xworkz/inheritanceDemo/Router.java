package com.xworkz.inheritanceDemo;

public class Router {

    public Router() {
        System.out.println("Router is initialized");
    }

    public void connectDevices() {
        System.out.println("Router connects multiple devices");
    }

    public void assignIP() {
        System.out.println("Router assigns IP addresses");
    }

    public void routeTraffic() {
        System.out.println("Router routes data traffic");
    }

    public void manageNetwork() {
        System.out.println("Router manages network settings");
    }

    public void firewallProtection() {
        System.out.println("Router provides firewall protection");
    }
}
