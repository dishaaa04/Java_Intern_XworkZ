package com.xworkz.inheritanceDemo;

public class LED {

    public LED() {
        System.out.println("LED is initialized");
    }

    public void emitLight() {
        System.out.println("LED emits light when current passes through");
    }

    public void saveEnergy() {
        System.out.println("LED consumes less energy");
    }

    public void longLasting() {
        System.out.println("LED has a long life span");
    }

    public void ecoFriendly() {
        System.out.println("LED is eco-friendly");
    }

    public void noHeat() {
        System.out.println("LED produces less heat");
    }
}
