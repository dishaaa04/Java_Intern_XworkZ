package com.xworkz.inheritanceDemo;

public class Traffic {

    public Traffic() {
        System.out.println("Traffic system initiated");
    }

    public void red() {
        System.out.println("Stop at red signal");
    }

    public void green() {
        System.out.println("Go at green signal");
    }

    public void yellow() {
        System.out.println("Slow down at yellow signal");
    }

    public void signals() {
        System.out.println("Follow the traffic signals");
    }

    public void zebra() {
        System.out.println("Use zebra crossing");
    }
}
