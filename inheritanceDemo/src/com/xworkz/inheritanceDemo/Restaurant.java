package com.xworkz.inheritanceDemo;

public class Restaurant {

    public Restaurant() {
        System.out.println("Restaurant is now open");
    }

    public void serveFood() {
        System.out.println("Restaurant is serving food");
    }

    public void takeOrder() {
        System.out.println("Restaurant is taking an order");
    }

    public void acceptPayment() {
        System.out.println("Restaurant is accepting payment");
    }

    public void cleanTables() {
        System.out.println("Restaurant staff is cleaning tables");
    }

    public void provideMenu() {
        System.out.println("Restaurant is providing a menu");
    }
}
