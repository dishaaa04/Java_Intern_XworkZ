package com.xworkz.inheritanceDemo;

public class Oven {

    public void powerOn() {
        System.out.println("Oven is powered on and preheating starts");
    }

    public void setTemperature() {
        System.out.println("Oven temperature is set for baking or roasting");
    }

    public void cookFood() {
        System.out.println("Oven cooks food with consistent heat");
    }

    public void turnOff() {
        System.out.println("Oven is turned off after cooking");
    }

    public void openDoor() {
        System.out.println("Oven door is opened to remove food");
    }
}
