package com.xworkz.inheritanceDemo;

public class PCB {

    public PCB() {
        System.out.println("PCB is created");
    }

    public void connectComponents() {
        System.out.println("PCB connects electronic components");
    }

    public void provideSupport() {
        System.out.println("PCB provides structural support");
    }

    public void routeSignals() {
        System.out.println("PCB routes electrical signals");
    }

    public void holdCircuits() {
        System.out.println("PCB holds circuits in place");
    }

    public void ensureInsulation() {
        System.out.println("PCB ensures proper insulation");
    }
}
