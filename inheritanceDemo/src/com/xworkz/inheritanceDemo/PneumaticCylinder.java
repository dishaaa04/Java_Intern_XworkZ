package com.xworkz.inheritanceDemo;

public class PneumaticCylinder {

    public void extend() {
        System.out.println("PneumaticCylinder extends when air pressure is applied");
    }

    public void retract() {
        System.out.println("PneumaticCylinder retracts to its original position");
    }

    public void convertAirToMotion() {
        System.out.println("PneumaticCylinder converts air pressure into linear motion");
    }

    public void controlSpeed() {
        System.out.println("PneumaticCylinder controls speed using air flow");
    }

    public void operateValve() {
        System.out.println("PneumaticCylinder operates using control valves");
    }
}
