package com.xworkz.inheritanceDemo;

public class Turbine {

    public void rotate() {
        System.out.println("Turbine rotates using wind, steam, or water");
    }

    public void generatePower() {
        System.out.println("Turbine generates mechanical or electrical power");
    }

    public void convertEnergy() {
        System.out.println("Turbine converts kinetic energy into usable energy");
    }

    public void spinBlades() {
        System.out.println("Turbine blades spin to produce motion");
    }

    public void maintainBalance() {
        System.out.println("Turbine maintains rotational balance");
    }
}
