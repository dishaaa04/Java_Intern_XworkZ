package com.xworkz.inheritanceDemo;

public class Lock {

    public Lock() {
        System.out.println("Lock is created");
    }

    public void secure() {
        System.out.println("Lock is securing the door");
    }

    public void open() {
        System.out.println("Lock is opened");
    }

    public void close() {
        System.out.println("Lock is closed");
    }

    public void rust() {
        System.out.println("Lock is rusting due to weather");
    }

    public void breakLock() {
        System.out.println("Lock is broken");
    }
}
