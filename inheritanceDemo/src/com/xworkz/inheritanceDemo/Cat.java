package com.xworkz.inheritanceDemo;

public class Cat {

    Cat() {
        System.out.println("A cute cat appears!");
    }

    public void meow() {
        System.out.println("The cat meows cutely");
    }

    public void sleep() {
        System.out.println("The cat sleeps for 18 hours a day");
    }

    public void eat() {
        System.out.println("The cat is eating some delicious tuna");
    }

    public void play() {
        System.out.println("The cat is playing with a ball of yarn");
    }

    public void scratchFurniture() {
        System.out.println("The cat scratches the new sofa");
    }
}
