package com.xworkz.inheritanceDemo;

public class LidarSensor {

    public void measureDistance() {
        System.out.println("LidarSensor measures distance using laser light");
    }

    public void detectObjects() {
        System.out.println("LidarSensor detects objects by emitting laser pulses");
    }

    public void scanArea() {
        System.out.println("LidarSensor scans the area to create a 3D map");
    }

    public void calibrateSensor() {
        System.out.println("LidarSensor calibrates for accurate distance measurement");
    }

    public void displayReading() {
        System.out.println("LidarSensor displays the distance or scanned data");
    }
}
