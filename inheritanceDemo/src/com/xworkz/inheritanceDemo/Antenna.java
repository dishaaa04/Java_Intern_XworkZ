package com.xworkz.inheritanceDemo;

public class Antenna {

    public Antenna() {
        System.out.println("Antenna is initialized");
    }

    public void transmitSignal() {
        System.out.println("Antenna transmits electromagnetic signals");
    }

    public void receiveSignal() {
        System.out.println("Antenna receives signals from space");
    }

    public void range() {
        System.out.println("Antenna has a specific transmission range");
    }

    public void orientation() {
        System.out.println("Antenna orientation affects performance");
    }

    public void frequencyBand() {
        System.out.println("Antenna works in a defined frequency band");
    }
}
