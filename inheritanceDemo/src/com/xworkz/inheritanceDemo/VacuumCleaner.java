package com.xworkz.inheritanceDemo;

public class VacuumCleaner {

    public void powerOn() {
        System.out.println("Vacuum cleaner is powered on");
    }

    public void startCleaning() {
        System.out.println("Vacuum cleaner starts cleaning the floor");
    }

    public void adjustSuctionPower() {
        System.out.println("Adjusting suction power based on surface type");
    }

    public void emptyDustBin() {
        System.out.println("Emptying the dust bin after cleaning");
    }

    public void powerOff() {
        System.out.println("Vacuum cleaner is powered off");
    }
}
