package com.xworkz.inheritanceDemo;

public class Stove {

    public void ignite() {
        System.out.println("Stove is ignited with gas or electric flame");
    }

    public void adjustFlame() {
        System.out.println("Stove flame is adjusted for cooking");
    }

    public void turnOff() {
        System.out.println("Stove is turned off after use");
    }

    public void heatUtensil() {
        System.out.println("Stove heats the utensil placed on it");
    }

    public void cookFood() {
        System.out.println("Stove is used to cook food using direct heat");
    }
}
