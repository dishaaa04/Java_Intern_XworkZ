package com.xworkz.inheritanceDemo;

public class Instrument {

    public Instrument() {
        System.out.println("Instrument is initialized");
    }

    public void play() {
        System.out.println("Playing a musical instrument");
    }

    public void tune() {
        System.out.println("Tuning the instrument");
    }

    public void repair() {
        System.out.println("Repairing the instrument");
    }

    public void store() {
        System.out.println("Storing the instrument safely");
    }

    public void amplify() {
        System.out.println("Amplifying the instrument's sound");
    }
}
