package com.xworkz.inheritanceDemo;

public class Transport {

    public Transport() {
        System.out.println("Transport system is active");
    }

    public void move() {
        System.out.println("Transport is moving");
    }

    public void stop() {
        System.out.println("Transport has stopped");
    }

    public void fuel() {
        System.out.println("Transport requires fuel");
    }

    public void capacity() {
        System.out.println("Transport has certain capacity");
    }

    public void route() {
        System.out.println("Transport follows a route");
    }
}
