package com.xworkz.inheritanceDemo;

public class RollingPin {

    public void prepareDough() {
        System.out.println("Rolling pin is used to flatten dough");
    }

    public void applyPressure() {
        System.out.println("Pressure is applied to roll out the dough evenly");
    }

    public void rotateDough() {
        System.out.println("Dough is rotated for even rolling");
    }

    public void cleanPin() {
        System.out.println("Rolling pin is cleaned after use");
    }

    public void storePin() {
        System.out.println("Rolling pin is stored properly for next use");
    }
}
