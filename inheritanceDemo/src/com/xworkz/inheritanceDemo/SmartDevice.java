package com.xworkz.inheritanceDemo;

public class SmartDevice {

    public SmartDevice() {
        System.out.println("Smart device is initializing");
    }

    public void powerOn() {
        System.out.println("Smart device is powering on");
    }

    public void connectToWiFi() {
        System.out.println("Smart device connecting to WiFi");
    }

    public void updateSoftware() {
        System.out.println("Smart device updating software");
    }

    public void showBatteryStatus() {
        System.out.println("Smart device showing battery status");
    }

    public void powerOff() {
        System.out.println("Smart device is powering off");
    }
}
